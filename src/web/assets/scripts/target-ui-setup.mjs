		setSelectedVesselMmsi,
		getSortTableBy,
		getMapFollowSelf,
		getDisableMapPanTo,
		getDisplayHarbours,
		setDisableMoveend,
		clearAlert,
		metersPerNm,
		maximumTargetRange,
		getCollisionProfiles,
		targetMaxAge,
		ageOutEnabled,
