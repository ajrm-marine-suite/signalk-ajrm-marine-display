import {
	configuredTargetRefreshConfig,
	configuredTargetRendererConfig,
	configuredTargetSelectionConfig,
} from "./target-ui-setup-configs.mjs";

export function createTargetUiRuntime({
	factories,
	pluginId,
	map,
	targets,
	boatMarkers,
	boatProjectedCourseLines,
	targetOverlays,
	labelCollision,
	rangeRings,
	autoCharts,
	harbourDisplay,
	autoProfileSettings,
	speechOutput,
	serverAlertEvents,
	targetSilence,
	selectionMarkers,
	modals,
	offcanvas,
	elements,
	getHttpResponse,
	initialPluginTargets,
	getSelfMmsi,
	getSelfTarget,
	setSelfTarget,
	getSelectedVesselMmsi,
	setSelectedVesselMmsi,
	getSortTableBy,
	getMapFollowSelf,
	getDisableMapPanTo,
	getDisplayHarbours,
	setDisableMoveend,
	clearAlert,
	metersPerNm,
	maximumTargetRange,
	getCollisionProfiles,
	targetMaxAge,
	ageOutEnabled,
	courseProjectionMinutes,
	showAlarmsInterval,
	alertPopup,
	replayStatusControls,
}) {
	let targetMapRenderer;
	const targetSelection = factories.createTargetSelection(
		configuredTargetSelectionConfig({
			pluginId,
			map,
			boatMarkers,
			targets,
			metersPerNm,
			getSelfMmsi,
			getSelectedVesselMmsi,
			setSelectedVesselMmsi,
			selectionMarkers,
			targetSilence,
			getHttpResponse,
			serverAlertEvents,
			getTargetMapRenderer: () => targetMapRenderer,
			clearAlert,
			requiredElement: factories.requiredElement,
			elements,
			modals,
			offcanvas,
		}),
	);

	targetMapRenderer = factories.createRenderer(configuredTargetRendererConfig({
		map,
		targets,
		boatMarkers,
		boatProjectedCourseLines,
		targetOverlays,
		labelCollision,
		rangeRings,
		autoCharts,
		harbourDisplay,
		autoProfileSettings,
		speechOutput,
		serverAlertEvents,
		targetSilence,
		targetSelection,
		selectionMarkers,
		modals,
		elements,
		getSelfMmsi,
		getSelfTarget,
		getCollisionProfiles,
		getSelectedVesselMmsi,
		setSelectedVesselMmsi,
		getSortTableBy,
		getMapFollowSelf,
		getDisableMapPanTo,
		getDisplayHarbours,
		setDisableMoveend,
		targetMaxAge,
		courseProjectionMinutes,
	}));

	const refreshController = factories.createRefresh(configuredTargetRefreshConfig({
		pluginId,
		map,
		getHttpResponse,
		targets,
		getSelfMmsi,
		setSelfTarget,
		getSelfTarget,
		targetSilence,
		serverAlertEvents,
		alertPopup,
		initialPluginTargets,
		targetMapRenderer,
		maximumTargetRange,
		targetMaxAge,
		ageOutEnabled,
		showAlarmsInterval,
		replayStatusControls,
	}));

	return {
		targetSelection,
		targetMapRenderer,
		refreshController,
	};
}
